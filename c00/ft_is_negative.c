#include <unistd.h>

void	ft_is_negative(int n)
{
	char a;
	char b;

	if (n >= 0 || !n)
	{
		a = 'P';
		write(1, &a, 1);
	}
	else
	{
		b = 'N';
		write(1, &b, 1);
	}
}
