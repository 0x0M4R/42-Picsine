int	ft_is_prime(int nb)
{
	int count;
	int i;

	count = 0;
	if (nb <= 1)
		return (0);
	i = 2;
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
